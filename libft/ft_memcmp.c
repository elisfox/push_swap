#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned const char		*a;
	unsigned const char		*b;
	size_t					i;

	i = 0;
	a = (unsigned const char *)s1;
	b = (unsigned const char *)s2;
	while (i < n)
	{
		if (a[i] != b[i])
			return (a[i] - b[i]);
		i++;
	}
	return (0);
}
